using System;

namespace Vici.Mvc
{
    public abstract class OrderedAttribute : Attribute
    {
        private int _order;

        public int Order
        {
            get { return _order; }
            set { _order = value; }
        }

        internal static void Sort(OrderedAttribute[] attributes)
        {
            Array.Sort(attributes, (att1, att2) => att1.Order - att2.Order);
        }
    }
}